# целые числа
print(1, 2, 0, -30, 99999, 0)

# дробные числа
print(1.0, 2.2, 0.4, -30.22222222, 99999.0, 0.0)

# сложение
print(2+2)

# вычитание
print(10-24)

# умножение
print(24*99)

# деление
print(10/5)
print(5/2)
print(5/2.0)
# print(1000/0)

# целочисленное деление
print(5//2)

# остаток от деления
print(5%2)

# составные операции
print(2+2*2)
print((2+2)*2)

# возведение в степень
print(5**2)